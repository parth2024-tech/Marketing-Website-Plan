type ClaimState = "idle" | "open" | "submitting" | "done" | "error";

function ClaimPanel({ id }: { id: string }) {
  const claimToken = typeof window !== "undefined"
    ? localStorage.getItem(`sentinel_claim_${id}`) ?? ""
    : "";

  const [state, setState] = useState<ClaimState>("idle");
  const [email, setEmail] = useState("");
  const [errorMsg, setErrorMsg] = useState("");
  const inputRef = useRef<HTMLInputElement>(null);

  // No claimToken = not the owner; don't show claim UI
  if (!claimToken) return null;

  if (state === "done") {
    return (
      <div className="surface-card rounded-xl p-5 flex items-center gap-3 border border-green-400/20">
        <Check className="w-4 h-4 text-green-400 shrink-0" />
        <div className="text-sm">
          <span className="text-foreground font-medium">Report saved.</span>{" "}
          <span className="text-muted-foreground">We'll email you when predictive alerts land.</span>
        </div>
      </div>
    );
  }

  if (state !== "open" && state !== "submitting") {
    return (
      <button
        onClick={() => { setState("open"); setTimeout(() => inputRef.current?.focus(), 50); }}
        className="w-full surface-card rounded-xl p-5 flex items-center justify-between gap-4 hover:border-primary/40 transition-all border border-border/60 text-left"
      >
        <div className="flex items-center gap-3">
          <Mail className="w-4 h-4 text-primary/60 shrink-0" />
          <div>
            <div className="text-sm font-medium text-foreground">Save this report to your account</div>
            <div className="text-xs text-muted-foreground mt-0.5">Get email alerts when predictive insights are ready.</div>
          </div>
        </div>
        <ChevronDown className="w-4 h-4 text-muted-foreground/40 shrink-0" />
      </button>
    );
  }

  const submit = async () => {
    if (!email.includes("@")) { setErrorMsg("Enter a valid email address."); return; }
    setState("submitting");
    setErrorMsg("");
    try {
      const res = await fetch(`/api/reports/${id}/claim`, {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify({ claimToken, email }),
      });
      if (res.ok) {
        setState("done");
      } else {
        const body = await res.json() as { error?: string };
        setErrorMsg(body.error ?? "Something went wrong.");
        setState("open");
      }
    } catch {
      setErrorMsg("Network error. Try again.");
      setState("open");
    }
  };

  return (
    <div className="surface-card rounded-xl p-5 border border-primary/20">
      <div className="flex items-center gap-2 mb-4">
        <Mail className="w-4 h-4 text-primary/60" />
        <span className="text-sm font-medium">Save this report</span>
      </div>
      <p className="text-xs text-muted-foreground mb-4 leading-relaxed">
        Enter your email and we'll link this report to your account — accessible from any device — and notify you when predictive health alerts are ready.
      </p>
      <div className="flex gap-2">
        <input
          ref={inputRef}
          type="email"
          value={email}
          onChange={(e) => setEmail(e.target.value)}
          onKeyDown={(e) => e.key === "Enter" && submit()}
          placeholder="you@example.com"
          disabled={state === "submitting"}
          className="flex-1 bg-background border border-border/60 rounded-lg px-3 py-2 text-sm text-foreground placeholder:text-muted-foreground/40 focus:outline-none focus:border-primary/60 disabled:opacity-50 transition-colors"
        />
        <button
          onClick={submit}
          disabled={state === "submitting" || !email}
          className="px-4 py-2 rounded-lg text-sm font-semibold text-background bg-primary hover:bg-primary/90 disabled:opacity-40 disabled:cursor-not-allowed transition-all shrink-0"
        >
          {state === "submitting" ? "Saving…" : "Save"}
        </button>
      </div>
      {errorMsg && <p className="text-xs text-red-400 mt-2">{errorMsg}</p>}
    </div>
  );
}


// Battery degradation chart driven by real battery data from the report.
// Plots the industry-standard expected degradation curve (from the engine's
// cycle anchors) and marks the user's actual current health point.
function BatteryDegradationChart({ battery }: { battery: { health?: number | null; cycleCount?: number | null } | null | undefined }) {
  // Expected degradation curve — matches cycleAnchors in engine.ts exactly
  const curvePoints = [
    { cycles: 0, expected: 100 },
    { cycles: 100, expected: 97 },
    { cycles: 200, expected: 92 },
    { cycles: 300, expected: 85 },
    { cycles: 500, expected: 75 },
    { cycles: 700, expected: 65 },
    { cycles: 1000, expected: 50 },
  ];

  const health = battery?.health;
  const cycles = battery?.cycleCount;

  // User's exact cycle position — always inserted so the dot is always visible
  const userPoint = cycles != null && health != null
    ? { cycles, expected: (() => {
        // Interpolate expected at user's exact cycle count from the curve
        for (let i = 1; i < curvePoints.length; i++) {
          const p0 = curvePoints[i - 1];
          const p1 = curvePoints[i];
          if (cycles <= p1.cycles) {
            const t = (cycles - p0.cycles) / (p1.cycles - p0.cycles);
            return p0.expected + t * (p1.expected - p0.expected);
          }
        }
        return curvePoints[curvePoints.length - 1].expected;
      })(), actual: health }
    : null;

  // Build chart data: merge curve points + user's exact position into a sorted series
  const baseData = curvePoints.map((p) => ({ cycles: p.cycles, expected: p.expected, actual: null as number | null }));
  const chartData = userPoint
    ? [...baseData, { cycles: userPoint.cycles, expected: userPoint.expected, actual: userPoint.actual }]
        .sort((a, b) => a.cycles - b.cycles)
    : baseData;

  // Determine how the user compares to expectations
  const gap =
    health != null && cycles != null
      ? (() => {
          // Interpolate expected value at user's cycle count
          for (let i = 1; i < curvePoints.length; i++) {
            const p0 = curvePoints[i - 1];
            const p1 = curvePoints[i];
            if (cycles <= p1.cycles) {
              const t = (cycles - p0.cycles) / (p1.cycles - p0.cycles);
              return (p0.expected + t * (p1.expected - p0.expected)) - health;
            }
          }
          return 0;
        })()
      : null;

  if (!battery || (health == null && cycles == null)) {
    return (
      <div className="surface-card rounded-2xl p-6 border border-border/60 mb-6 flex items-center justify-center h-48">
        <p className="text-sm text-muted-foreground/50">Battery data not available for this system</p>
      </div>
    );
  }

  return (
    <div className="surface-card rounded-2xl p-6 border border-border/60 mb-6">
      <div className="flex flex-wrap items-center justify-between gap-4 mb-5">
        <div>
          <h3 className="text-sm font-semibold text-foreground mb-1">Battery Degradation Curve</h3>
          <p className="text-xs text-muted-foreground">
            Your battery vs. expected health at {cycles ?? "?"} charge cycles
            {gap != null && gap > 5
              ? ` · ${gap.toFixed(0)}pts below expected — degrading faster than average`
              : gap != null && gap < -5
              ? ` · ${Math.abs(gap).toFixed(0)}pts above expected — degrading slower than average`
              : " · degrading at expected rate"}
          </p>
        </div>
        {health != null && (
          <div className={`px-3 py-1.5 rounded-lg text-xs font-semibold border ${
            health >= 80 ? "border-emerald-500/40 text-emerald-400 bg-emerald-500/10" :
            health >= 60 ? "border-yellow-500/40 text-yellow-400 bg-yellow-500/10" :
            "border-red-500/40 text-red-400 bg-red-500/10"
          }`}>
            {health.toFixed(1)}% actual health
          </div>
        )}
      </div>

      <div className="h-52 w-full">
        <ResponsiveContainer width="100%" height="100%">
          <ComposedChart data={chartData} margin={{ top: 5, right: 10, left: -25, bottom: 0 }}>
            <CartesianGrid strokeDasharray="3 3" stroke="#ffffff10" vertical={false} />
            <XAxis
              dataKey="cycles"
              stroke="#ffffff40"
              fontSize={10}
              tickMargin={8}
              tickFormatter={(v) => `${v}cy`}
            />
            <YAxis domain={[40, 100]} stroke="#ffffff40" fontSize={10} tickFormatter={(v) => `${v}%`} />
            <Tooltip
              contentStyle={{ backgroundColor: "#0f172a", borderColor: "#334155", borderRadius: "8px", fontSize: "11px" }}
              itemStyle={{ color: "#e2e8f0" }}
              formatter={(value: any, name: string) => [
                `${Number(value).toFixed(1)}%`,
                name === "expected" ? "Industry baseline" : "Your battery",
              ]}
              labelFormatter={(label) => `${label} cycles`}
            />

            {/* Replace zone: health < 60% */}
            <ReferenceArea y1={0} y2={60} fill="#ef4444" fillOpacity={0.05} />
            <ReferenceLine y={60} stroke="#ef4444" strokeDasharray="4 4" strokeOpacity={0.4}
              label={{ value: "Replace threshold", position: "insideTopRight", fill: "#ef4444", fontSize: 9, opacity: 0.7 }} />

            {/* Expected degradation curve */}
            <Line
              type="monotone"
              dataKey="expected"
              stroke="#ffffff35"
              strokeWidth={2}
              dot={false}
              name="expected"
              strokeDasharray="5 5"
              isAnimationActive={true}
              animationDuration={800}
            />

            {/* User's actual data point */}
            <Line
              type="monotone"
              dataKey="actual"
              stroke="#22d3ee"
              strokeWidth={0}
              dot={(props: any) => {
                if (props.value === null || props.value === undefined) return <></>;
                return (
                  <motion.circle
                    key="user-dot"
                    cx={props.cx}
                    cy={props.cy}
                    r={7}
                    fill="#22d3ee"
                    stroke="#0f172a"
                    strokeWidth={2}
                    initial={{ scale: 0, opacity: 0 }}
                    animate={{ scale: 1, opacity: 1 }}
                    transition={{ delay: 0.9, type: "spring", stiffness: 250, damping: 18 }}
                  />
                );
              }}
              activeDot={{ r: 9, fill: "#22d3ee" }}
              name="actual"
              isAnimationActive={false}
            />
          </ComposedChart>
        </ResponsiveContainer>
      </div>

      <div className="mt-4 flex items-center gap-6 text-xs border-t border-border/30 pt-4">
        <div className="flex items-center gap-2 text-muted-foreground/60">
          <span className="w-6 h-px border-t-2 border-dashed border-white/30 inline-block" />
          Industry baseline
        </div>
        <div className="flex items-center gap-2 text-primary/80">
          <span className="w-3 h-3 rounded-full bg-cyan-400 inline-block" />
          Your battery
        </div>
        {gap != null && Math.abs(gap) > 3 && (
          <div className={`ml-auto font-medium ${gap > 0 ? "text-amber-400" : "text-emerald-400"}`}>
            {gap > 0 ? `▼ ${gap.toFixed(0)}pts below curve` : `▲ ${Math.abs(gap).toFixed(0)}pts above curve`}
          </div>
        )}
      </div>
    </div>
  );
}


