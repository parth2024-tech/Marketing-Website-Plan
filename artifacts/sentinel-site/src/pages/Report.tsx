/**
 * Renders an interactive circular health score indicator ring with standard Apple-style
 * easing transitions.
 * 
 * @param score Overall computed hardware score.
 */
function ScoreRing({ score, size = 148 }: { score: number; size?: number }) {
  const strokeW = 11;
  const r = (size - strokeW) / 2;
  const circ = 2 * Math.PI * r;
  const color = score >= 80 ? "#22d3ee" : score >= 60 ? "#f59e0b" : "#f87171";
  const glowColor = score >= 80 ? "#06b6d4" : score >= 60 ? "#d97706" : "#ef4444";

  const [displayed, setDisplayed] = React.useState(0);

  React.useEffect(() => {
    import("framer-motion").then(({ animate }) => {
      animate(0, score, {
        duration: 1.0,
        ease: [0.25, 0.1, 0.25, 1],
        onUpdate: (val) => setDisplayed(Math.round(val)),
      });
    });
  }, [score]);

  const dash = (displayed / 100) * circ;

  return (
    <div className="relative shrink-0" style={{ width: size, height: size }}>
      {/* Ambient glow behind ring */}
      <div
        className="absolute inset-0 rounded-full"
        style={{ background: `radial-gradient(circle, ${glowColor}18 0%, transparent 70%)` }}
      />
      <svg
        width={size}
        height={size}
        viewBox={`0 0 ${size} ${size}`}
        className="rotate-[-90deg] absolute inset-0 z-0"
      >
        {/* Track */}
        <circle
          cx={size / 2}
          cy={size / 2}
          r={r}
          fill="none"
          stroke="currentColor"
          strokeWidth={strokeW}
          className="text-border/30"
        />
        {/* Glow arc (avoids Safari drop-shadow transform drift bug) */}
        <circle
          cx={size / 2}
          cy={size / 2}
          r={r}
          fill="none"
          stroke={color}
          strokeWidth={strokeW}
          strokeDasharray={`${dash} ${circ}`}
          strokeLinecap="round"
          style={{
            filter: "blur(6px)",
            opacity: 0.6,
            transition: "stroke-dasharray 0.05s linear",
          }}
        />
        {/* Progress arc */}
        <circle
          cx={size / 2}
          cy={size / 2}
          r={r}
          fill="none"
          stroke={color}
          strokeWidth={strokeW}
          strokeDasharray={`${dash} ${circ}`}
          strokeLinecap="round"
          style={{
            transition: "stroke-dasharray 0.05s linear",
          }}
        />
      </svg>
      {/* Score text — centred inside ring (only rendered here, NOT duplicated outside) */}
      <div className="absolute inset-0 flex flex-col items-center justify-center z-10" style={{ transform: 'translateZ(0)' }}>
        <div
          className="text-4xl font-black tabular-nums leading-none"
          style={{ color, textShadow: `0 0 20px ${color}60` }}
        >
          {displayed}
        </div>
        <div className="text-[11px] font-mono text-muted-foreground/50 mt-0.5">/100</div>
      </div>
    </div>
  );
}

